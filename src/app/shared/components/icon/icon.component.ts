export class IconComponent {
  private translationService = inject(TranslationService);

  @Input() name!: SemanticIconName | string;
  @Input() size: string | number = '20';
  @Input() color?: string;
  @Input() ariaLabel?: string;
  @Input() ariaHidden: boolean = true;

  iconName = computed<SemanticIconName>(() => this.name as SemanticIconName);
  
  get iconClass(): string {
    const isRTL = this.translationService.getCurrentLanguage() === 'ar';
    const baseClasses = 'transition-transform duration-200';
    const rtlClasses = isRTL && this.shouldMirror() ? 'scale-x-[-1]' : '';
    return `${baseClasses} ${rtlClasses}`.trim();
  }

  private shouldMirror(): boolean {
    // Icons that should be mirrored in RTL
    const mirrorIcons: string[] = ['arrows-left-right', 'chevron-down'];
    return mirrorIcons.includes(this.name);
  }
}

